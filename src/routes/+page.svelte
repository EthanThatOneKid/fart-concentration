	<h2>Instructions</h2>

	<p>
		Concentration is a memory game where you match pairs of cards. Click on a card to reveal its
		sound. Click on another card to reveal its sound. If the sounds match, the cards stay face up.
		If the sounds do not match, the cards flip back over. The game is over when all cards are face
		up!
	</p>

